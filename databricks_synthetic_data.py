entity_rows = []
entity_counter = [0]  # mutable counter

def gen_entity_id(name):
    """Generate a deterministic entity ID from the name."""
    h = hashlib.md5(name.encode()).hexdigest()[:6].upper()
    return f"E{h}"

def walk_org(tree, parent_name, parent_id, level):
    """Recursively walk the org tree and generate entity rows."""
    for child_name, subtree in tree.items():
        child_id = gen_entity_id(child_name)
        entity_rows.append(Row(
            Parent_Entity_ID=parent_id,
            Parent_Entity=parent_name,
            Child_Entity_ID=child_id,
            Child_Entity=child_name,
            Entity_Level=level
        ))
        entity_counter[0] += 1
        if isinstance(subtree, dict):
            walk_org(subtree, child_name, child_id, level + 1)
        elif isinstance(subtree, list):
            # Leaf level: list of sub-unit names
            for sub in subtree:
                sub_id = gen_entity_id(sub)
                entity_rows.append(Row(
                    Parent_Entity_ID=child_id,
                    Parent_Entity=child_name,
                    Child_Entity_ID=sub_id,
                    Child_Entity=sub,
                    Entity_Level=level + 1
                ))
                entity_counter[0] += 1

